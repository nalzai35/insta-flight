خواص عقیق خراسانی بر بسیاری از افراد روشن است. بسیاری از ائمه (ع)، خواص عقیق را توصیف نموده اند. اما باید به این نکته توجه داشت که عقیق خراسانی علاوه بر سلامتی جسم و تاثیرات مادی خود، باعث شادابی روح و روان می شود. اما ویژگی های این سنگ ارزشمند عبارتند از:

۱- ازدیاد ثواب عبادات: انگشتر عقیق باعث افزایش عبادات از جمله نماز می شود. این سنگ خیر و برکت را در زندگی افزایش داده و رزق و روزی بیشتر می شود.

۲- برآورده شدن حاجات: به گفته معصومین (ع)، دستی که انگشتر عقیق در آن است، در هنگام دعا خالی نمی ماند و حاجات آن بر آورده می شود.

۳- دوری از گناه: یکی دیگر از خواص عقیق خراسانی، مصونیت انسان از گناه و کراهت است. این سنگ زینتی همچنین انسان را از خطرات نیز مصون می دارد.

۴- عاقبت بخیری: انگشتر عقیق به عاقبت بخیری و خوش شانسی منجر می شود و انسان را از اتفاقات ناگوار حفظ می کند.

