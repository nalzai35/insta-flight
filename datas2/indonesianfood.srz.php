BAGAR TETELAN & DAGING SAPI
by: @aras_galeri

Bahan:
250gr daging sapi bagian sengkel
250gr tetelan sapi
1 batang sereh
4 lbr daun jeruk
2 lbr daun salam
1 lbr daun kunyit
1/2 sdm pemasak kambing (bisa di skip)
*ini bumbu rempah khas masakan padang, silahkan googling
2 buah kapulaga
1 bunga lawang
1 asam kandis

Bumbu halus:
2 sdm cabe giling halus dari cabe merah kriting
10 cabe rawit merah *bisa ganti lada bubuk 1/2sdt
1 sdm ambu-ambu (bisa di skip)
*detail baca di note bawah
12 siung bawang merah 
6 siung bawang putih 
1 sdm lengkuas giling 
1/2 sdm jahe giling 
1/2 sdm ketumbar 
Garam

Caranya
1. Rebus daging & tetelan sekitar 10menit lalu buang air lemaknya
2. Tumis bumbu halus, daun2, bunga lawang dan kapulaga sampai harum, lalu masukkan potongan daging, aduk sampai bumbu rata, tambahkan air matang sampai daging kerendam, masukkan asam kandis.
3. Setelah mendidih masukkan pemasak kambing, kecilkan api, tutup wajan, masak sampai daging matang dan kuah mengental sekitar 1jam, trakhir tambahkan sejumput gula atau kaldu bubuk sesuai selera

NOTE - Ambu-ambu: dari kelapa parut yang di sangrai sampai kering dan kecoklatan trus di ulek sampai halus dan berminyak, ini yg bikin masakan jadi wangi dan enak
- kalo gak punya asam kandis bisa ganti tomat
- bisa di sajikan dengan taburan bawang goreng.
