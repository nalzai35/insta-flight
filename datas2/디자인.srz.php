이쪽을 바라 보고 있다.
소망의 귀환을 기대하며, 
기다리며… 8월의 폭염횡단 뒤안길로
돌아 와야하고, 낮은 숨의 포옹을
기다리는 9월의 삼십날이 있고!
  
이 살가운 일상이 빠진 
지상의 삶 또한 무슨 의미 있으랴 
안녕, 9월의 시간이여
꿈에라도 너를 찾아 돌아오려마. 
  
  
September  Sonata
/ Lee Kyae Jin
  
Cool of autumn winds p​ermeating through me, 
“It’s your place more than now.” 
As I sit on the bench under the 
hope stop arch, waiting for a high 
  
Top to take me over the day hills, 
With eight months of my lifeyear 
In hand; and today I see a 
Young passion moving in that hour 
  
Beyond the wide life field. Now, it 
Raises it’s hand to forehead as if 
Trying to see some happy land more clearly, 
Waiting, expecting… What, then, 
  
Is the meaning of life on earth 
Without this comforting ritual--a hour 
Awaiting the return of those who belong to it? 
Hi, September, I will visit you 
often in dream! 
  
