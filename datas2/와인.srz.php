​
프리울라노 2017은 처음 마주한다.
​
색감만 본다면 열대과실미가 물씬 감돌 것 같은 14K 류의 골드 컬러.
​
오픈 직후의 노즈에서는 파인애플과 헤이즐넛 뉘앙스를 필두로 노란 사과, 흰꽃, 허브, 그리고 패션프룻의 힌트도 살짝.
​
팔렛에서는 미네랄과 더불어 직관적으로 풍성하게 피어오르는 파인애플, 살구, 감초, 헤이즐넛의 풍미. 산미는 팔렛으로 흘러들어올 때의 감각을 놓고 볼때 결코 부족함 없이 느껴지지만 뭉근한 과실맛, 그리고 미네랄과 잘 어우러진 덕분에 마일드하게 다가온다. 그동안 다른 빈티지들이 보여줬던 모습보다 확실히 섬세한 편.
​
은근한 유질감, 은은한 스모크와 그레이프 프룻, 화약과 스모크 내음, 그리고 옥수수 수염차 느낌이 감도는 애프터 테이스트. 
​
여러 의미에서 매력적이다. 마시면서 내내 "이야~" 하며 감탄을 금치 못했던..
​
비유는 좀 이상하지만 예전에 2009 포스카리노를 가장 맛있게 마셨던 2014년 여름의 어느 날, 그리고 플라네타 샤르도네 2010을 가장 맛있게 마셨던 2014년 봄의 어느 날이 문득 Remind되던 순간. (사실 플라네타 샤도는 요즘보다 수년 전의 스타일이 훨씬 밸런스가 좋았고 맛도 더 좋았다고 생각한다. 오키함은 덜하지만 오히려 그래서 더 좋았던.. 좀더 시칠리아 와인다웠달까..)
​
온도가 올라가면 지릿함과 알콜 기운이 슬슬 올라오니 비교적 낮게 해서 마실 것을 권장. 재어 보진 않았지만 내가 마신 온도는 느낌상 11~12도 부근인 것으로 추정된다.
