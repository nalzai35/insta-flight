byクックパッド
レシピID「5392174」
を参考に作りました。

🌟過程と感想
新しいお弁当箱です。セリアのもの♡笑
いつも使っているお弁当箱がすこし詰めづらくて
今回みたいな形のが欲しいな〜と探していたら
セリアさんで見つけたので購入してみました✌️
この形で詰め方慣れたら、ちゃんとしたもの買いたいなぁ。

今日の中身はサバの味噌煮と酢豚は昨日一昨日の残り物。
サバは調理する際に霜降りをすることで青臭さをなくして、
お弁当に入れる時も汁気をきっています。
自分の分なのでいいや〜と思って入れましたが
きになる方もいらっしゃいますよね。
そこのセーフラインは結構別れそう…。
私も人に持たせるとしたら入れないかも…悩ましい。笑

また、今日は卵焼きがきれいな色でできてすっごく嬉しい！！！！
中心部は穴が空いてしまったけれど、いつも焦げてしまうから嬉しくて嬉しくって。
備忘録として作り方を残します。
✳️卵焼き(個人の備忘録)
①卵2個にお砂糖大さじ1とマヨネーズ小さじ1を入れる。
②卵を泡立てないように切るように混ぜる(これが大事！！！！)。お箸で卵を掴み持つように何度も繰り返して、さらっと卵が流れるまで混ぜる。
③油を引いたフライパンorテフロン製のフライパンに2回に分けて入れる。

今まで卵を混ぜるのが雑で色むらができていたんだろうな。
きれいな卵焼き作る上では②が本当に大事だと思う！
次は中身がギュッと詰まった卵焼きにするぞーーー！！

💫卯の花
分量通り調理。
ねぎはなし。

うーーーーーん😥
分量通りレシピ通り作ったはずなのに、
パサパサでおからがまとまらず…。
水を追加したりもしたのですがだめでした。
おからの扱い素人なので何が悪いのかもわからない。
結構残っているから、ポテトサラダとかにして和えて消費しなきゃ。

🌟反省点
・卵焼きの中身が崩れた。
→1回目に入れた卵液が少なかった。
・卯の花失敗。
→火加減が強すぎた？なんでだろう。不明。

