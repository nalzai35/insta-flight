❇️Zeppole ﻿
源自Campania大區的拿坡里，有時也被稱為frittelle。傳統上是在每年3月19日慶祝聖約瑟夫節時食用的節慶甜點，聖約瑟夫是耶穌的父親，因此當天也被視為父親節，據說聖約瑟夫是木匠和炸物攤販的主保聖人，這也是為甚麼這個節日的慶祝點心大多都是油炸物。﻿
19世紀的美食家博恩维奇诺公爵在1837年著作的食譜書中詳文紀錄了zeppole di San Giuseppe的做法，因而受到義大利貴族們喜愛並普遍流傳於義大利。Zeppole與一般泡芙中的粉水比例(1:2)不同，Zeppole的粉水比例是1:1，因此口感比較硬。﻿
