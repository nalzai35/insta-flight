Q :: 블랙워터에서 사용하는 원두는 어떤 원두를 사용하고있으며 
프랜차이즈와 비교했을때 차이점이 무엇인가요?

A :: 우선 생두의 등급에는 조금씩 다르긴하지만 크게. 
스페셜티 ( C.O.E. & MICRO LOT & Special ) 그 아래로 하이커머셜( 프리미엄)
그 아래로  커머셜 이라는 등급이 존재합니다

즉 스페셜 - 프리미엄 - 커머셜 이렇게 크게 나눌수 있습니다.

근래 커피시장의 발달로 인하여 스페셜티 커피를 사용한다는 곳을 쉽게 발견할수 있습니다.

그러나.

그 안의 내용을 자세히 들여다 봐야합니다.

스페셜티 5% 커머셜 95%를 사용하여  스페셜티 블렌딩이라는 이름을 사용할수 있다는 점입니다.

이를 알수 있는 방법은 딱히 없지요.

그래서 말씀드렸다 싶히 소비자도 현명한 소비.

즉 바리스타에게 다가가 어떠한 커피를 사용하며 블렌딩이라면 어떠한 블렌딩을 했는지 물어봐야합니다.

프랜차이즈의 커피와 비교를 하기엔 너무나 많은 사실들이 노출이 될 위험이 있습니다.

하지만. 대형으로 찍어내고 중간 유통. 그리고 마지막 가맹점주들에게 들어가기까지 비용이 존재하기에

처음 사용된 생두의 퀄리티를 기대하기는 어렵습니다.

하지만 개인 매장. 즉 저희 블랙워터에서는 자체적인 로스팅. 로스팅 후 디개싱( 가스를 빼는 작업)까지 마친후 판매날짜를 지키며.

100% 스페셜티 만을 사용하고 있습니다.

물론 스페셜티 만을 사용한다 해서 옳다고 볼수는 없습니다.

하지만 좋은 재료를 사용하여 합리적인 가격. 그 가격에 맞는 커피를 제공해 드린다고 자부할수는 있습니다.

