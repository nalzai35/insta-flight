Nasi Bakar Tuna Kemangi memiliki rasa pedas dengan aroma harum kemangi dan daun jeruk bikin sajian ini jadi spesial!
.
Bahan : untuk 4 Porsi
800 gr nasi putih
Daun pisang untuk membungkus
.
Tuna Kemangi :
3 sdm minyak goreng
8 lembar daun jeruk, buang tulang daunnya, iris halus
250 g fillet ikan tuna, potong dadu 1 cm
2 buah cabai merah besar, buang biji, iris tipis
4 buah cabai rawit, iris tipis
2 batang daun bawang, iris tipis
50 ml air
75 g daun kemangi
1 sdt air jeruk nipis
.
Bumbu:
8 butir bawang merah
4 siung bawang putih
4 buah cabai merah
2 cm jahe
2 batang serai bagian putih, iris tipis
3 sdt garam
50 ml air
.
Cara Membuat :
1. Panaskan minyak goreng dalam wajan, masukkan bumbu halus dan daun jeruk, aduk hingga harum. Masukkan ikan tuna, aduk hingga ikan berubah warna.
2. Tambahkan cabai dan daun bawang, aduk hingga cabai layu. Masukkan air, tambahkan daun kemangi dan air jeruk nipis, aduk terus hingga seluruh bahan cukup matang.
3. Masukkan nasi, aduk hingga tercampur rata, angkat segera.
4. Bagi nasi menjadi 4 bagian, lalu bungkus setiap bagian dengan selembar daun pisang. Sematkan kedua ujungnya dengan lidi.
5. Panaskan alat pemanggang atau wajan grill di atas api, taruh nasi bungkus di atasnya. Panggang hingga aromanya harum, angkat.
.
TIPS:
Ikan tuna dapat diganti dengan ayam, udang ataupun jamur.
