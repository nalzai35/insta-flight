「弟はまだ小さくて、おばあちゃん子だから」という理由で、僕だけ親戚中をたらい回しにされ、小学生に上がったぐらいから、毎年のように転校していました。
「優也はお兄ちゃんだから、大丈夫。」って言い聞かせられて、自分の気持ちを押し殺して、聞き分けの良い子を演じる。
養護施設に預けられたこともあるし、父の再婚相手とも合わずに、放り出されたこともある。父の再婚相手との間に、妹が2人生まれて、両親がいることを、羨ましく思った事を今でも覚えてる。
家は、子どもの僕にでもわかるぐらい、貧乏な家庭で、小6の僕に『夜逃げ』を手伝わせる始末。親戚のおじさんには、何かと理由をつけては怒られて、よく手をあげられて、顔の骨が陥没したって理由で入院したこともあった。恥ずかしくて、病院では言えなかったけど。。。
よく家出を繰り返して、友達の家にお世話になったことを今でも覚えてる。
18歳の時に、1人暮らしが半強制的に始まって、奨学金借りてバイトしまくって、学校に行ってた。
親戚からは、「本書けるぐらい波乱万丈やな。」って言われるけど、今思うと、大人ならではの事情もあったんだろうし、今更追求するつもりもない。
仕事柄、様々な障がいを持つ方と毎日会うけど、五体満足で好きな事に熱中できてるだけで、幸せだと思っています。

こんな話、酔っぱらった延長で、嫁と信頼してる人にしか話したことないけど、不幸自慢してる人に、まずこの話よりエグい話ある？っていつも思う。
「そんな事グチグチ言う前に、行動してみろ！そしたら笑い話に変わるから。」っていつも言ってます。

③［コンプレックスは解消される危険がある］ですが、『貧乏』を売りにして、共感を集めていたら、人気が出て『お金持ち』になると、共感できなくなる。
『モテない』を売りにして、共感を得ていたら、人気が出て『モテる』と共感できなくなる。
共感できなくなると、応援する意味を失う。結果、先が短い。解消されないのなら、そもそも売り方を間違っているのかも。

上記のように、結果コンプレックスに頼ってると、自分のモチベーションを上げるエネルギーが切れる事がある。という話です。

まとめますね。
「不幸自慢を売りにしてたら、どこかでエネルギーが無くなっちゃうよ！」です。

そもそもネガティブな話ばっかりする人の周りに、ポジティブな話をする人が集まるわけがないですよね。
その時点で、スタートに遅れてる事になる。

僕の過去を、少し書きましたが、別に普段は笑い話にしてる話なんで、気にしないでね。
家族間も、別に普通なんで。笑
僕が笑い話に出来るようになったのは、過去を言い訳の材料にすることを、辞めたからです。
誰だって、人に知られたくない過去ぐらいある。僕も人の顔色ばかり伺って、嘘ばっかりつく子どもでした。
その過去があるから、嘘つきは直ぐに見抜けるし、嘘からは、何も生まれないと知れた。
今では、正直者過ぎて、モメることもあるけれど、前よりかは、よっぽどいい。

ごちゃごちゃ言う前に、行動したら勝手に環境が変わるから。そんなあなたを、受け入れてくれる人は必ずいる。
僕が今、笑い話にできるのは、そんな方たちのおかげです。頑張ろうね。

今日も一日頑張りまーす！

