⭕ 사전 검증(Soft Proof)
1) 언어 패턴: 불평 → 수요
ᆞGoogle Trends 등

2) 채널 리서치
ᆞ아마존 등
▪️상품 찾아보기, 매출 순위 확인, 물량 점검
