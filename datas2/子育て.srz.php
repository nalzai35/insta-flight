子どもが癇癪などを起こした時、見守る(意識的無視)、取り合わない、という関わりをしていた時に感じたこと。
この時「私は見守っているから」と、自分の本当の部分を隠し、本当は心の奥では遮断していたのかもしれない。
「見守る」を、知識や努力でやろうとしていたのかもしれない。
私が本当にしていることは何だろう。
見守る、だろうか？
見守るという仮面をかぶった無視(遮断)かのしれない。
見守ることの本質は【繋がり】だと気付いたのは6年前でした。
繋がりをつくった上での見守りは、信頼と「すべては大丈夫」という安心感がありました。
見守るというのは尊い技能であって、表面的に安易に使われるべきではなかったのかもしれない、と感じました。
繋がりがベースにあるから成り立つ。
この尊い非言語のコミュニケーションが、見守ることの本質だと。
そのままの自分自身で関わっていいんだ、ということ。
私が自分自身で関わること以上に子どもたちが望むものは何もないのだということ。
そのままの自分自身で人と繋がり合う経験は、言葉では表現できない喜びでした。
この繋がりを感じたとき、見守ることの本質を、頭ではなく、経験的に理解した瞬間でした。
