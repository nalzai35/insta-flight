1998年のディズニー・アニメ「ムーラン」の実写化です。

◼物語
国家の存亡がかかった戦を前に、各家庭の男子を兵士として差し出せとの命令が下り、息子のいないファ家の一人娘ムーランは、老いた父親を守るため男に成り済まして戦地に行く。というお話です。

◼ほほうな話👴🏻💡✨
・ドニー・イェン、ジェット・リーなどアジアのアクションスターが出演しています。
・当時16歳だったクリスティーナ・アギレラがアニメ版から約20年の時を越え再びリフレクションを歌っています。

◼アニメとの比較
大筋はムーランでしたが、アニメ版ムーランに登場したキャラクターはムーラン、ムーランの父母、皇帝くらいしか出てきませんでした。それぞれの性格もアニメ版とはやや異なります。アニメ版ムーランが好きな人でこの映画を楽しみにしている人はそこが少し残念かもしれません。歌もなく陽気さやロマンスも薄く、女性社会、強い女性、という想いが前に出てきています。雰囲気的にはディズニー版キングダムという印象です。

◼値段について
値段は3280円。最初に再生してから48時間の間なら何度でも観れます。

値段に関しては最初は高いな～と思いましたが、人によるかな～と思います☺

例えば友達と割り勘して観るとかならすごく安いと思うし、あと僕もそうですけどディズニー映画って結局、字幕と吹替えとか複数回観る事が多いのでそう考えると安いです。好きなシーンだけリピートとかもできますし(笑)

なので上記に当てはまり、自宅にそこそこの再生環境をお持ちの方はめっちゃ有りだと思いました👍🏻✨

一人で観る、スマホやPCで観るという方にはぜひ！とは言いづらいですね☺(笑)

