به گزارش ایرنا، «سعید خطیب زاده» شامگاه پنجشنبه در واکنش به انتشار تصاویر توهین آمیز به پیامبر اسلام (ص) توسط مجله «شارلی ابدو» این اقدام موهن را قویاً محکوم کرد. 

