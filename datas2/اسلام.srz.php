سخنگوی دستگاه سیاست خارجی افزود: اقدام موهن نشریه فرانسوی که به بهانه آزادی بیان تکرار شده، موجب جریحه دارشدن احساسات موحدان عالم، اقدامی تحریک آمیز و توهین به ارزش های اسلامی و عقاید بیش از یک میلیارد مسلمان در جهان است. 

خطیب زاده در پایان تأکید کرد: برخلاف اقدام موهنی که توسط این مجله انجام شده، آزادی بیان، ارزش والایی است که باید به شکل سازنده‌ای در جهت همزیستی مسالمت آمیز نوع بشر و تفاهم بیشتر میان ادیان به کار گرفته شود.

