오늘은 엔도카나비노이드에 대해 얘기해볼게요
인용 시작합니다
⠀
인간진화에 대한 연구의 일환으로 레이츨런은
러너와 개, 흰 족제비에게서 달리기 전후의
엔도카나비노이드 농도를 측정했다.
레이츨런은 달리기를 한 후 인간과 개의
엔도카나비노이드 농도는 증가하지만
흰 족제비의 경우는 그렇지 않았다는 사실을 발견했다
이 연구결과는 인간의 진화과정에서
달리기가 맡고 있는 역할을 설명하는
‘달리기 위해 태어난 존재’라는 이론을 뒷받침한다.
현대인과 개들의 조상은 음식을 얻기 위해 달려야만 했지만
흰 족제비들은 그럴 필요가 없었기 때문이다.
⠀
기분을 좋게 하는 엔도카나비노이드에 대한 연구를 하기 위해
레이츨런은 러너들이 네 가지 강도에서 30분간 운동을 하도록
설계했다.
그는 최대심박수의 70% 수준(‘조깅하는 속도’라고 해석된다.)과
80% 수준(대화를 나누며 꾸준히 달릴 수 있는 속도)으로
달리기를 했을 때 엔도카나비노이드가
최대한으로 증가한다는 것을 발견했다.
엔도카나비노이드 농도는 레이츨런 연구의 러너들이
최대심박수의 50%의 수준(걷기)으로 뛰거나
90% 수준으로 30분간 뛸 때 줄어들었다.
엔도카나비노이드는 고맙게도
평소 가볍게 달리는 정도로 달릴 때 가장 효과적으로 생성된다.
보통 때처럼 평범하게 달리고 나면
더 좋은 기분으로 돌아올 수 있게 되는 것이다.
⠀
#나는달리기로마음의병을고쳤다.
⠀
엔도카나비노이드 시스템은
통증, 식욕, 기분, 기억, 면역력에 중요한 역할을 합니다.
마리화나는 이 시스템을 건드리는 대표적인 인공 물질입니다.
우리 몸에는 anandamide와 2-AG 라는 물질을 만들어 이 시스템을 활성화 시킵니다.
세로토닌 도파민과 달리 요건 세간에 잘 알려지지 않은 이유는
이 시스템을 매개로 하는 상용화된 항우울제가 없기 때문입니다.
간혹 내과에서 엔도카나비노이드 시스템을
활성화 시키는 약제를 쓰기도 하는데
암환자에서 통증과 우울증을 개선하는 데에
도움이 된다는 보고도 나오고
섬유근육통처럼 만성통증 개선에도
역할은 한다는 보고가 있습니다.
⠀
실제로 우울증은 통증을 잘 몰고 다닙니다.
우울증 걸리면 마음만 괴로운게 아니라
통증에 대한 감수성도 올라가는데
여기에 여러 메카니즘이 작용하지만
엔도카나비노이드 시스템도 작용을 합니다.
얼핏 보면 위로와 공감만으로 우울증을
다 해소할 수 있을 것 같지만
우울증은 엔도카나비노이드 시스템 기능불순도 내포하고 있어
말만으로 통증 식욕 기억력 등이 쉽게 회복되지 않은 경우도 많이 있습니다.
⠀
암튼 달리기가 이 시스템쪽 건강에 도움이 된다는 것.
그리고 내가 기분이 좋아지고 통증을 풀기 위해서
죽자사자 뛸 필요 없다는 것.
기록에 연연해 하지 않고 내 안의 기분 변화에 주목하는 것.
