Traducción: sánguche de milanesa 😂 Pero no cualquiera 💁‍♀️
El katsu sando es una comida al paso típica de Japón. Generalmente se hace con cerdo o pollo; pero en este caso es de carne, por eso el gyu. Y dentro de la versión con carne, muchas veces se hace con wagyu. Debe quedar increíble, pero por esta vez se los debo 🤣
Estas son principales diferencias con un sandwich de milanesa argentino: la carne es más gruesa (1-2 cm), se reboza en panko, y se usa pan de molde. Se acompaña con salsa tonkatsu, que muchos consideran la barbacoa japonesa. Se compra hecha pero también se puede reemplazar con algunos ingredientes que tengamos en casa 👆 Yo la hice con 2 cdas de ketchup, 2 cdas de salsa inglesa, 1 cdita de salsa de soja y 1/2 cdita de azúcar.
El sandwich también suele llevar repollo y mostaza. Para el pan, es ideal usar un pan de leche japonés 🍞 (yo usé el de @labsucre). Normalmente se le retira la corteza, yo no lo hice porque era bastante fina. Como no tenía panko, lo preparé con el mismo pan: corté 2 rodajas (ahí sí es importante sacar la corteza), las tosté, las procesé (no muy fino) y las volví a tostar.
Como el bife es grueso, hay que darse un poco de maña con la fritura, para que no se arrebate y quede en el punto deseado (a mi me quedó más cocido de lo que quería 🙄). La próxima vez probaría hacerlo al horno (con el tip de tostar el panko con un poquito de aceite).
Les paso la receta (para uno). Igual es super simple y casi todo es c/n 😋

1 bife de ojo de bife (250 g aprox)
1 huevo
harina
panko
2 rodajas gruesas de pan de molde
manteca
repollo
salsa tonkatsu

🔹Pasar el bife por harina, luego por huevo batido, y luego por panko.
🔹Cocinar frito o al horno, hasta el punto deseado.
🔹Untar los panes con manteca de un lado (el lado que irá adentro del sandwich), y tostar de ambos lados.
🔹Armar el sandwich en el siguiente orden: pan, milanesa, salsa, repollo, pan.
🔹Poner un peso por encima y dejar reposar 5 minutos (se comprime un poquito y de paso se asienta la carne).

