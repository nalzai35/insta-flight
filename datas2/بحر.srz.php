3- يساعد في الحفاظ على:
أ. نظارة الشعر
ب. نقاء وصفاء الاظافر
ج. نظارة البشرة وحمايتها من التشققات
4- يساعد في تخفيض معدل الكوليستيرول

3-  مشروب لايت 
