1- الوفيرا جيل ( مشروب الصبار)
الفوائد والمزايا:

1- يحتوى على 18 حمض امينى و20 فيتامين ومعدن ضرورى لوضائف الجسم
2- يعمل على تحسين الحالة الصحية لمرضى قرحة المعده وعسر الهضم والتهاب القولون والسكر وضغط الدم وتصلب الشرايين
3- يعمل على زيادة المناعة الطبيعة للجسم .ومنشط عام طبيعى
4- يخلص الجسم من السموم والجزور الحرة التى لها دور كبيرا فى الامراض المزمنة
5- يعمل على ترميم خلايا الجسم واعادتها لوضعها الطبيعى

2-  جارسينيا

الفوائد والمزايا: 
1- يعمل على فقدان الشهية
2- مفيد في مراقبة الوزن والمحافظة عليه
3- يساعد في الحفاظ على:
أ. نظارة الشعر
ب. نقاء وصفاء الاظافر
ج. نظارة البشرة وحمايتها من التشققات
4- يساعد في تخفيض معدل الكوليستيرول

3-  مشروب لايت 
الفوائد والمزايا:

1- يغطي احتياجات الجسم من الفيتامينات والمعادن
2- بديل صحي عن وجبات الطعام
3- يوفر موازنة دقيقة بين البروتين والوحدات الحرارية
4- لاتوجد إضافات حافظة

4- مشروب (FIBER )

الفوائد والمزايا: 
1-يساعد على تحسين وظيفةالهضم
2-عمل على المحافظة على نسبة السكر بالدم .
3-عمل ابطاء امتصاص الطعام مما يعطى احساس دائم بالشبع
4-يعمل على ازالة السموم .من الجسم
5-ليس لة طعماو رائحة ويتم اذابتة فى الماء كليا

5 – حبوب (THERM)

الفوائد والمزايا:

1- تعزيز مستويات الطاقة بالجسم
2- الاسراع من جهود فقدان الوزن حتى ترى نتائج سريعة وواضحة
3- يساعد على زيادة التميثيل الغذائى .وتعظيم ما تبذلونةمن مجهود عضلى
4- يحتوى على خلاصةكل من الشائ الاخضر كمضاداكسدةقوى ويعمل على حرق الدهون المخزنة
5- يحتوى على مستخلصة القهوة الخضراء اللذى يعمل على دعم مستويات السكر بالدم بالمعدل الطبيعى وكبح الشهية مع رفع النشاط العام 
6- يحتوى على مجموعة كاملة من فيتامينات B وCواللتى تدعم حرق الدهون والكربوهيدات المخزنة وخاصة خلال ممارسة الرياضة
