1- الوفيرا جيل ( مشروب الصبار)

الفوائد والمزايا:

1- يحتوى على 18 حمض امينى و20 فيتامين ومعدن ضرورى لوضائف الجسم
2- يعمل على تحسين الحالة الصحية لمرضى قرحة المعده وعسر الهضم والتهاب القولون والسكر وضغط الدم وتصلب الشرايين
