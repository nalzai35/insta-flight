よい状態の肌は、水分と皮脂が混ざり合ってなめらかなうるおいのベールが肌をおおっています。
しかし、肌が乾燥した状態のまま放置してしまうと、水分が足りないため、うるおいバランスが崩れてしまいます。
混ざるための水分が足りないため、皮脂だけが目立つ状態になるのです。
それがテカりや崩れの原因に😢

②汗
夏、大量の汗をかくと、しっかり密着していないメイクは流れてしまいます。
また、汗で肌のうるおいのベールを流してしまい、乾燥してしまうことで、肌そのものが、メイクが密着しにくい状態になっていることもあります💦

