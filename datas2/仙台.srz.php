メイクをする前は化粧水や乳液、クリームなどで肌を整えてから行います。
しかし、スキンケアをつけた直後、まだなじみ切っていない状態ですぐにメイクをするのはあまりよくありません。
スキンケアの直後は肌の表面に、水分や油分が残っている状態なのです。
