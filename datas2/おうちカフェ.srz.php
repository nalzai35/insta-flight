【材料】 2人前
ごはん　　　　　　　400g
玉ねぎ　　　　　　　1個
鶏もも肉　　　　　　200g
卵（Mサイズ）　　　2個
サラダ油　　　　　　大さじ1
.
----- 調味料 -----
しょうゆ　　　　　　大さじ2
酒　　　　　　　　　大さじ2
みりん　　　　　　　大さじ2
砂糖　　　　　　　　大さじ1
顆粒和風だし　　　　小さじ2
.
かいわれ大根　　　　適量
.
【手順】
かいわれ大根の根元は切り落としておきます。
1. 玉ねぎは薄切りにします。
2. 鶏もも肉は一口大に切ります。
3. ボウルに卵を入れて溶きます。
4. フライパンにサラダ油を入れて中火で熱し、2を入れて炒めます。
5. 鶏もも肉の色がかわったら、1を入れて玉ねぎがしんなりするまで中火のまま炒め、調味料を加えます。
6. 中火で煮込み鶏もも肉に火が通り、汁気が半分ほどになったら3の2/3の量を流し入れます。
7. 少し固まってきたら、残りの3を入れ、中火で10秒程したら火から下ろします。
8. 器にごはんを盛り付け、7をのせ、かいわれ大根を飾ったら完成です。 
.
【コツ・ポイント】 
卵を2回に分けていれることでとろとろに仕上がります。
玉ねぎの食感は切り方によって変わります。くし切りにする事で、しゃきしゃきとした食感が増しますので、お好みでお試しください。
ごはんはお好みの量で調節してください。
ご高齢の方や、2才以下の乳幼児、妊娠中の女性、免疫機能が低下している方は、しっかりと加熱し卵の生食を避けてください。 
