某次我到東京工作，
原定想到荒川線的荒川車庫參觀一下，
結果沒時間去，回港後就將這地方遺忘了。
後來再到東京，酒店很近都電荒川線，
再看真地圖，發現其中一站就是我遺忘了的車庫，
太好了，上帝總是會滿足一下我的小願望。
網上指，當日車庫其實不開放，
但我即管試試，反正重點是遊荒川線的電車河，
沿途有很多其他東西可以看、可以玩。
.
我在比較早的站下車，因想在民居中先散步，
突然有人呼喚我名字，我還以為是幻聽，
怎可能在這陌生的國度會有人認識我？
誰知真的是朋友！而且她還陪著男朋友來，
大家都很驚訝，來個自拍，
而他們聽了我的目的地，也想去車庫看看。
因為這次是獨遊，我又不懂日文，
所以好幾天幾乎沒跟人說話，
太好了，可以有朋友吹吹水，
而她的男朋友是美國人，也傾得投契呢！
.
走了十多分鐘，終於到達荒川車庫前，
這個戶外展覽館竟然開放！
有位非常親切的管理員穿得一身車長制服，
來招呼在場大約只有兩個小家庭和我們三人。
場內有兩卡舊電車，
分別在1954年製造的5500型，
還有1962年製造的舊7500型，
在5500型車箱中放置了很多舊照片相集和雜誌，
可以一睹荒川線的老東京樣貌。
而我始終最深印象的，
無論日本的博物館所陳列有多文化的東西也好，
總是有「破壞」風景的卡通人物來襯托一下，
荒川車庫前也不例外！
起初到日本遊覽時我會覺得這些格格不入，
但當發現動漫和Q版卡通能貫穿生活每個層面，
甚至有人形容過動漫就是日本人的神，
那麼，我明了，這根本是不可分割的文化了！
