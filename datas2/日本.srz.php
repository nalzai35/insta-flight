在日本平安時代⋯ ​
某日，天有異象出現紅月，霎那間天色詭譎！群魔亂舞百妖做亂，而一名名為伊郎丸的鐵匠，為了救出女兒，將自己的靈魂與惡怪進行交易，同時也踏上了無法回頭之路，匠鬼就此誕生⋯ .​
​
此時！發現異樣的皇軍，也派遣了武士將軍前去斬殺魔妖「牛鬼蜘蛛」；但善良的將軍為了保護女孩，不慎被牛鬼所殺。 ​
​
而成為了妖怪的匠鬼，在尋找女兒的途中，撞見了四處做亂的牛鬼蜘蛛並將其殲滅！怎知殘留在牛鬼蜘蛛的「生命魂」，轉而寄生在戰敗將軍屍體內，變成了強大的牛鬼武士⋯ ​
​
另一處，努力對抗魔妖入侵的善良妖怪「天狗」，帶著妖怪夥伴河童保護村民們，且為了設下封印與結界，犧牲了自己的右手；但也因此邂逅匠鬼，並收他為徒。 ​
而天狗為了減緩匠鬼靈魂被吞噬的時間，特地贈與一達摩護符予匠鬼，用於吸收身上的邪惡力量，但護符吸收太多邪惡力量，貌似起了異變⋯ ​
​
而隨著時間推移，以往的邪惡妖怪早已被殲滅殆盡，天狗及匠鬼們，為了日後人世間的平安，便與玩具獵人及封印師阿佐協議，透過「玩具」及象徵永不放棄的「達磨」，將祂們滿滿能量封印其中，並散落於各地保護人民；從此「妖怪達磨」就此誕生！ ​
