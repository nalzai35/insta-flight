【三杯鶏】
・鳥もも肉　2枚
・ニンニク　3かけ
・生姜　1片
・鷹の爪　2本
・ごま油　大2
・醤油　大2
・紹興酒(又はお酒)大2
・氷砂糖　2個(又は砂糖大1)
・台湾バジル(又はタイバジルや普通のバジルでも) 適量

① 鶏肉の水気を取って一口大にカット。土鍋やストウブ鍋、普通の蓋付きの鍋にごま油、細かく切ったニンニクと生姜を入れて香りが立ってきたら鶏肉を入れて炒める。
② 大体鶏肉に火が通ったら紹興酒、砂糖、醤油、鷹の爪を入れて炒め蓋をして弱火で10分。最後にバジルを入れてかき混ぜしんなりしたら出来上がり！
