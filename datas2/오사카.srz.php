うぶ毛が突然濃くなったりするのと同様、背中ニキビも、ストレスやホルモンバランスの乱れがまず一つの要因です。

その他に言われているのが、髪の毛のコンディショナー等のすすぎ残しによる界面活性剤の付着。ニキビは毛穴が詰まってしまうこともニキビができる要因になります。

毛は汗や汚れを溜まりやすくなる原因でもあるので、その毛を取り除く事で、ニキビの予防はもちろん改善にも繋がります。

なので、ニキビをなくす3つのポイントは

🤍脱毛で毛をなくす
🤍コンディショナーなどをしっかりとながす
 (身体よりもまず髪を先に洗う！！)
🤍界面活性剤が使われていないボディソープをつかう

