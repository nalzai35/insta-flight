逃げる事はしたくないタイプだから
何があっても行くスタイルだったけど今のままじゃお金とか時間の無駄やし
そこまで自分が考えるなんて相当メンタルやられてたわ。
いまだに0か100か理論なんで。
やると決めたらやるし、やらんと決めたら全くやらんし、やるなら金かけてでも効率よくやりたいし極端な性格ほんま恨むわ（笑）
