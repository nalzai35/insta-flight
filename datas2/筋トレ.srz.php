本気で1ヶ月おさまらんかった。

理由が分からん。かった。

まいとれさんと話して、自分でも考えて、
多分「今の自分にMAXから痩せた自分に満足してしまってるから」
っていう結論。
確かにその通り。
まだ痩せたい、まだ体重落としたい
っていう気持ちは心の何処かにはあるけどそれと一緒で
もう頑張ったな、めっちゃ痩せたよな
っていう気持ちがあったのも確か。
なんでこんなしんどい思いをしてまでダイエットしてるんやろ？
なんで好きなときに好きなもの食べれん生活してるんやろ？
今の生活って楽しいんか？わくわくするんか？充実してるんか？
