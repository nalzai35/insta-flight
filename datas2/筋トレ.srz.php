食欲が止まらないし、
お腹が満たされないし、
食べても食べてもお腹が空くから冷蔵庫漁るし、
そんな状態で買い物に行くからお菓子は買いまくるし、
お菓子のつまみ食いは止まらないし、
別にひと口くらいいけるわって食べるし、
てか今までもお菓子食べて痩せれたしその分動けばいいやって考えに至ったし、
でも運動嫌いが続くかって言われるとすぐ飽きるし、
なんなら暑いから動きたくないし←、
いつもの食事量で満足しないからクソほど食べるし、
報告の写真より食べてるし、
それに麻痺しだすから平気で低糖質のものじゃなくて普通のカロリーのものも食べだすし、
