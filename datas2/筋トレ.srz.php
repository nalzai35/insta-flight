そろそろボディメイクに入るんだってよ。万年ダイエッターだと思ってた自分がボディメイクだってよ。てか万年デブだと思ってた自分がもう普通の人なんだってよ。なんなら今の体脂肪率でRIZAPに通おうと思う人いないんだってよ。今やめたらもったいないけどもう終わりでもいいですよって言われるぐらいなんだってよ。
