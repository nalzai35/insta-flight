.
季節の変わり目は天候や気温が不安定で、食欲が落ちたり、生活リズムが崩れたりとトラブルが増える時期です。毎日の食事で体がもとめる栄養をしっかり摂取し、ダメージに負けない体を保ちたいですね。
.
少しでも効率よく、そして美味しく栄養を摂取したい。それなら、ぜひ「スーパーフードの代表・大豆」と「栄養たっぷり・トマト」の組み合わせをどうぞ。
.
たんぱく質の含有量で知られている大豆ですが、それ以外にも、私たちの健康に欠かせない５大栄養素すべてが含まれており、さらに「必須アミノ酸」もとても豊富！その凄さは、必須アミノ酸９種類すべてが含まれていることからも頷けます。まさに自然の完全栄養食。「畑のお肉」と呼ばれる所以ですね。
.
そしてトマトは、抗酸化ケアにうれしいリコピンや、グルタミン酸、クエン酸などが豊富な野菜。ビタミンやミネラルも豊富なので、疲れたときや元気を出したいときにぜひ摂りたい食材なのです。
.
この２つを美味しく手軽に、そしてヘルシーに食べられるまーめんアレンジ、ぜひご賞味ください。
.
