🍲 Ingredientes 🍲
(1 xícara de chá):
🔸200ml de óleo;
🔸2 gemas;
🔸1 colher (sopa) de suco de limão;
🔸1 e 1/2 colher (sopa) de mostarda (de preferência a de Dijon);
🔸1/3 xícara (chá) de folhas de salsinha lavadas e secas;
🔸1 dente de alho socado;
🔸Sal a gosto.
-
🍛 Modo de Preparo 🍛
🔹Coloque os quatro primeiros ingredientes no copo do processador, coloque o mixer desligado dentro do copo e pressione contra o fundo;
🔹Ligue e bata por 15 segundos sem levantar, quando a consistência começar a ficar como a da maionese, vá levantando para as laterais, sem desligar ou parar de bater;
🔹Vá girando, fazendo movimentos de baixo para cima, até atingir um molho consistente, desligue;
🔹Adicione a salsinha, o alho e tempere com sal, bata mais um pouco, até virar uma mistura homogênea;
🔹Armazene em geladeira e sirva com sanduíches, salgados, em patês e afins.
