Pictured is the beautiful Hiraoka Tree Art Center in Sapporo, Japan. If you're planning a trip to Japan in the fall, be sure to save this post, as this location is an absolute must-see.
•
Other than western and Japanese gardens, the park is home to the iconic, crimson red Japanese Maple Tree (Momiji 紅葉). As pictured, these blooming trees are incredibly beautiful and vibrant in color. 
•
While the park is open from late April through early November, the best time to go is in the later half of fall. That being said, if you'd like to see a striking red view like this, plan for either late October or early November.
•
If you're taking public transportation, follow the below steps.
•
🍁 Oyachi Station (Orange-Colored Tozai Line)
🍁 Above-Station Bus Terminal (Bus Every 30 Minutes)
🍁 Bus 69 (Approximately 15 minutes)
🍁 2 Chome Hiraoka 4-Jo (Exit Bus)
🍁 Hiraoka Tree Art Center (Hiraoka Jugei Centre)
•
Note that once you exit the bus, this final destination is around 100 meters walking distance.
