《冷凍→レンジ解凍 100g設定 強度2→トースト3分》
🎬手割り動画あり(pic.5)
プレーン生地に、たっぷりのあんことクリームチーズ、ある程度の大きさに砕かれた胡桃がゴロゴロ💓
クラストは、リベイク時にトースターの熱がよく当たっていたトップ周辺はパリカリッ、側面などはぎぃーっと引きちぎる系。
クラムは、まだ少しでも温かさが残っているうちは、ふかふんわりもちゅ。完全に冷めると、コシが出て、ふわもっちぃーもちゅねちゅみちのお餅系食感に。
個人的に、完全に冷めた後のこのお餅みたいなもっちゅーっとした食感が特に好き❣️
中のあんこは、甘さの度合いは一般的な感じで、小豆の皮などがある程度残った粒あんではあるもののホクホク感よりはねっとり感のほうが強めのタイプ。
クリームチーズは、酸味も甘みもあまり強くなく(悪い意味では全然ない)、バターみたいな香りが強いタイプ。
このクリームチーズのバターに近い香りとあんこが合わさって、あんバターっぽい感じの味わいになっていて美味しい😻
そこにゴロゴロ入っている胡桃の香りや食感も良い感じ💕
