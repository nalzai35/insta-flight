El mesociclo de base: estos mesociclos normalmente se utilizan al comienzo de cada período. Se basan en la creación de una base física sobre la cual se trabaje posteriormente. Aunque este periodo se caracteriza por ser de menor intensidad, también lo hace por el gran volumen de trabajo que tendrá el deportista.

