天井を作る際に、考えたこと。

構造はカラマツの無垢材。
カラマツ材の間には、
タモの無垢材にスリット（溝）を入れたボードを嵌め込んでいる。溝に光が走り、影をつくる様子が美しい。

現在、見えている下垂木は実際には210mmあるので、本来は配線スペースをとっても150mm前後はみせることができる。しかし、あえて30mmしか見せていない。カラマツの強い表情に対して、タモ材をしっかり拮抗させたかったのが理由である。無駄なこと、矛盾と思える選択が、美しさにつながれば嬉しい。

