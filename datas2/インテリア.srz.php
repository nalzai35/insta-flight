When building the ceiling, we thought about it.

The structure is solid larch wood.
Between the larch wood.
A board with a slit in it is inlaid into the solid wood of Tamo.The light runs into the grooves and creates a beautiful shadow.

The rafters you can see now are actually 210mm long, so even with the wiring space, it should be possible to show about 150mm.However, we dared to show only 30mm of wiring space.The reason for this is that we wanted to make the timber to compete with the strong look of the larch.We are happy if this seemingly useless or contradictory choice leads to beauty.

