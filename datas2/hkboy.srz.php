乳癌治療新突破？有澳洲研究指出，特定濃度蜜蜂毒液能殺死乳腺癌細胞，而對正常細胞影響少。與目前的化療藥物混合使用，有效減低老鼠體內腫瘤的生長。
.
哈里珀金斯醫學研究所和西澳大利亞大學合作進行研究，提取了來自西澳州珀斯的312隻蜜蜂和大黃蜂的毒液，以測試毒液對各種類型乳腺癌的療效， 當中包括治療選擇有限的三陰性乳腺癌。
.
結果發現，特定濃度的蜜蜂毒液可以在60分鐘內完全殺死的三陰性乳腺癌細胞及含有大量人類表皮生長因子受體2(HER2)的乳腺癌細胞，而對體內正常細胞的影響甚微。
.
對此，西澳大利亞大學博士Ciara Duffy指，毒液中一種叫做蜂毒素（melittin）的成分，其可於20分鐘內，干擾並封閉癌細胞主要的信息傳遞通路，而這些通路對於癌細胞的生長和復製是至關重要的。
.
另外，研究亦指，蜂毒素或可促進化學療法的作用，他們將蜂毒素與現有化療藥物如紫杉醇混合使用，發現蜂毒素可刺穿癌細胞膜，令化學藥物可更好進入細胞，用於在白老鼠身上，有效減少其體內腫瘤的生長。研究結果已發表於《自然精準腫瘤學》期刊上。
.
