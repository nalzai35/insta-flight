#四男こーちゃん﻿
﻿
昨日はこーちゃんの外科外来でした。﻿
術後の傷口がうんでしまった所の経過を診てもらいました。﻿
﻿
前日くらいにやっとかさぶたのようになっていたので、もう大丈夫そうだなと思っていたのですが、先生的にも良い感じですねと言っていただけて一先ず安心😌﻿
💩も毎日よく出ているし、経過良好です🌈﻿
﻿
傷口を清潔に保つために、絆創膏などを毎日2.3回替えてお風呂でよく洗うよう言われていました。﻿
ただ、毎日絆創膏を替えると周りの皮膚が負けてしまい可愛そうで色々と試行錯誤の上、ガーゼとマイクロポアロテープとキャビロンに辿り着きました。﻿
﻿
テープを剥がす時には、ベビーオイルなどでふやかしてからするといいと退院前に看護師さんに教えていただいていたのですが、それでも負担がかかっていそうなのと時間がかかってしまって本人もバタバタし始めるので、皮膚用リムーバーを導入してとても良かったです。﻿
数滴でスルリと負担なくはがれるので、お互い気持ちよくケア出来ています。﻿
このまま傷口が綺麗になるといいな🌈﻿
﻿
病院から帰って来たこーちゃんは、疲れ果ててちょっと険しい顔をして長いこと爆睡していました。﻿
起きてお家にいる事がわかったら、いつもの優しい顔に戻って、あった事を沢山お話ししてくれました。﻿
この子も相手の目を見てニコニコと話が長いタイプ☺️（あおちゃんもそう。なおちゃんは大きな独り言が多いタイプ。笑）﻿
﻿
お話のバリエーションも増え、表情も豊かになり、日に日に少しずつふっくらしてきて、ますます可愛くなっているこーちゃんです👶🏻（親バカ）﻿
